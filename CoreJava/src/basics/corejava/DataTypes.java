package basics.corejava;

public class DataTypes {

	public static void main(String[] args) {
		/*
		 * PRIMITIVE DATA TYPES INT,FLOAT,CHAR,DOUBLE,LONG,BOOLEAN,SHORT,BYTE
		 */
		int a = 50;
		float b = 50.6f;
		char c = 'a';
		double d = 1325.89;
		long l = 145556465l;
		boolean bo = true;
		short s = 45;
		System.out.println("Primitive DataTypes is \n" + "" + a + "\n" + b + "\n" + c + "\n" + l + "\n" + bo + "\n" + d
				+ "\n" + s);

		/*
		 * DERIVED DATA TYPES OR NON PRIMITIVE DATA TYPES STRINGS,ARRAYS
		 */

		String s1 = "vinay";
		int[] array = new int[4];
		DataTypes datatypesinjava;
		System.out.println("Derived DataTypes is\n" + "String is" + s1 + "\n" + array);
	}

}
